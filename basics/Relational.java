package operators;

public class Relational {

	public static void main(String[] args) {
		int a=20;
		int b=10;
		

		System.out.println("a="+a);
		System.out.println("a="+b);
		

		System.out.println("a<b="+(a<b));
		System.out.println("a>b="+(a>b));
		System.out.println("a<=b="+(a<=b));
		System.out.println("a>=b="+(a>=b));
		System.out.println("a!=b="+(a!=b));
		

		

	}

}
