package operators;

public class Increment_operator {

	public static void main(String[] args) {
		
int a = 10, b = 20 ,c;
		
		c = ( ++a ) + ( ++b );
		
		
		
		System.out.println( " Value of a = " + a );
		System.out.println( " Value of b = " + b );
		System.out.println( " Value of c = " + c );
	}

}
